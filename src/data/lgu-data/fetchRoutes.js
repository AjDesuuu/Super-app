async function processSegments() {
    const enrichedSegments = [];

    console.log("🚀 Starting Route Fetching Sequence...");

    for (const segment of INPUT_SEGMENTS) {
        const start = segment.points[0]; 
        const end = segment.points[1];   

        const startStr = `${start[1]},${start[0]}`;
        const endStr = `${end[1]},${end[0]}`;

        const url = `http://router.project-osrm.org/route/v1/driving/${startStr};${endStr}?overview=full&geometries=geojson`;

        console.log(`   📍 Fetching realistic path for: ${segment.name}...`);

        try {
            const response = await fetch(url);
            const data = await response.json();

            if (data.routes && data.routes.length > 0) {
                const highResPoints = data.routes[0].geometry.coordinates.map(c => [c[1], c[0]]);
                enrichedSegments.push({
                    ...segment,
                    points: highResPoints
                });
            } else {
                console.warn(`   ⚠️ No path found for ${segment.name}, keeping straight line.`);
                enrichedSegments.push(segment);
            }
        } catch (error) {
            console.error(`   ❌ API Error for ${segment.name}:`, error.message);
            enrichedSegments.push(segment);
        }

        await new Promise(r => setTimeout(r, 500));
    }

    const fileContent = `
// --------------------------------------------------------
// AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.
// Generated by: fetchRoutes.js
// --------------------------------------------------------

export interface RoadSegment {
    id: string;
    name: string;
    color: string;
    status: string;
    count: string;
    points: [number, number][]; // Array of [lat, lng]
}

export const roadSegments: RoadSegment[] = ${JSON.stringify(enrichedSegments, null, 4)};
`;

    // Save to the current folder (since you are running it from inside lgu-data)
    fs.writeFileSync('./roadSegments.ts', fileContent);
    console.log("✅ DONE! roadSegments.ts has been updated.");
}

processSegments();